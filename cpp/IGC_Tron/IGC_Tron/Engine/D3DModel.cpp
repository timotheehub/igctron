	D3DModel::D3DModel( D3DRenderer* _renderer ) : IModel( _renderer )
	{
		renderer = _renderer;
	}

/***********************************************************************************/
/** METHODES PUBLIQUES                                                            **/
/***********************************************************************************/

	void D3DModel::render()
	{
		if ( dirty ) update();

		renderer->getDevice()->SetTransform( D3DTS_WORLD, (D3DMATRIX*)&matWorld );

		if ( mesh != NULL ) mesh->render();

		IModel::render();
	}

