/***********************************************************************************/
/** METHODES PUBLIQUES                                                            **/
/***********************************************************************************/

	void OGLModel::render()
	{
		if ( dirty ) update();

		glPushMatrix();
		glMultMatrixf( (GLfloat*)&matWorld );

		if ( mesh != NULL ) mesh->render();

		glPopMatrix();

		IModel::render();
	}

